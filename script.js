
    // Add JavaScript logic here
    document.getElementById('submitBtn').addEventListener('click', function() {
      var side1Selection = document.getElementById('side1Dropdown').selectedOptions;
      var side2Selection = document.getElementById('side2Dropdown').selectedOptions;
      var report = document.querySelector('input[name="reportRadio"]:checked').value;
      
      if (side1Selection.length > 0 && side2Selection.length > 0) {
        var result = '';
        if (report === 'Negative') {
          result = '<button class="btn btn-success result-button">প্রেগন্যান্সি টেস্ট পজিটিভ</button>';
        } else if (report === 'Positive') {
          result = '<button class="btn btn-danger result-button">প্রেগন্যান্সি টেস্ট নেগেটিভ</button>';
        }
        document.getElementById('result').innerHTML = result;
      } else {
        alert('Please select options for both sides.');
      }
    });
  

